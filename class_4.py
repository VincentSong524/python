import turtle as t


t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.penup()
t.goto(30, 20)
t.setheading(0)
t.pendown()
t.forward(40)
t.left(120)
t.forward(40)
t.left(120)
t.forward(40)
t.left(120)
t.penup()
t.goto(20, 80)
t.pendown()
t.dot(10, "black")  
t.penup()
t.goto(80, 80)
t.pendown()
t.dot(10, "black")
t.done()